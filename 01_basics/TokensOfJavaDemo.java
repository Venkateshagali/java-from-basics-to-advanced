public class TokensOfJavaDemo {
    public static void main(String[] args) {
        // Keywords
        int number = 10;
        double price = 99.99;

        // Identifiers: number, price, TokensOfJavaDemo
        // Literal: 10, 99.99, "Java"
        String language = "Java";

        // Operators and separators
        int result = number + 5;

        System.out.println("Language: " + language);
        System.out.println("Result: " + result);
    }
}
